     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      $request["user_id"] = Auth::id();
      $this->validate($request, Stock::$rules);
      $form = $request->all();
      unset($form['_token']);
      Stock::create($form);
      return redirect('/');
    }

